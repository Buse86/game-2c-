"""
Генератор карты — процедурное создание каньона.
Каньон формируется синусоидой, проходящей через центр карты.
Клетки, удалённые от центральной линии, становятся стенами.
"""

import math
import random
from src import config as cfg
from src.navigation import NavigationEngine


class MapGenerator:
    """Статический класс для генерации карты каньона."""

    @staticmethod
    def generate_canyon(start, goal, world_seed):
        """Создаёт карту каньона по заданному сиду (зерну генерации).

        Алгоритм:
        1. Инициализируем генератор случайных чисел сидом
           (один и тот же сид всегда даёт одну и ту же карту — для сохранений)
        2. Генерируем параметры синусоиды (частота, амплитуда, фаза)
        3. Для каждого столбца вычисляем центральную линию каньона
        4. Клетки далеко от центра помечаем как стены
        5. Проверяем, что путь от старта до цели проходим

        Аргументы:
            start      — точка входа врагов (x, y) на сетке
            goal       — точка базы (x, y) на сетке
            world_seed — целое число-сид для генератора случайных чисел

        Возвращает:
            set клеток-препятствий, или пустой set если путь непроходим
        """
        blocked = set()

        # Фиксируем сид — один сид = одна карта (воспроизводимость)
        random.seed(world_seed)

        # Случайные параметры синусоиды определяют форму каньона:
        freq = random.uniform(0.3, 0.5)    # частота — как часто каньон изгибается
        amp = random.uniform(2.5, 4.5)     # амплитуда — насколько сильно отклоняется
        offset = random.randint(0, 100)     # фаза — сдвиг синусоиды по горизонтали

        # Проходим по каждому столбцу карты
        for x in range(cfg.cols):
            # Центральная линия каньона — синусоида вокруг середины карты
            # sin(x * freq + offset) даёт значение от -1 до 1
            # Умножаем на амплитуду и прибавляем к середине
            center_y = int(cfg.rows / 2 + math.sin(x * freq + offset) * amp)

            # Ограничиваем, чтобы каньон не уходил за края
            center_y = max(2, min(cfg.rows - 3, center_y))

            # Для каждой клетки в столбце решаем: стена или проход
            for y in range(cfg.rows):
                # Если клетка далеко от центральной линии — это стена
                # random.choice([2, 3]) добавляет неровность краям каньона
                if abs(y - center_y) > random.choice([2, 3]):
                    # Не блокируем точку входа и базу
                    if (x, y) != start and (x, y) != goal:
                        blocked.add((x, y))

        # Проверяем проходимость: если путь заблокирован — возвращаем пустой set
        # (вызывающий код сгенерирует новый сид и попробует снова)
        nav = NavigationEngine()
        if not nav.is_passable(start, goal, blocked):
            return set()

        return blocked
